nombre_completo = input("Introduce tu nombre completo: ")

nombre_minusculas = nombre_completo.lower()

nombre_mayusculas = nombre_completo.upper()

nombre_titulo = nombre_completo.title()

print(f"En minúsculas: {nombre_minusculas}")
print(f"En mayúsculas: {nombre_mayusculas}")
print(f"Con la primera letra de cada palabra en mayúscula: {nombre_titulo}")
